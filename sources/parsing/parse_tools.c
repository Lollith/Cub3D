#include "cub3d.h"

int	ft_skip(char *line, int *i)
{
	while (line[*i] == ' ' || line[*i] == '\t' || line[*i] == '\n'
		|| line[*i] == '\v' || line[*i] == '\f' || line[*i] == '\r') // a confirmer
		(*i)++;
	return (0);
}

